weather = "rer"
case weather
when 'sunny'
puts 'Shorts and flip-flops for the win!'
when 'cloudy'
puts "Bring an umbrella, just in case."
when 'rainy'
puts 'Cuddle up in your yoga pants. We might be in for some flooding.'
when 'snowing'
puts 'Pull out your snow boots and go build a snowman!'
when 'foggy'
puts "Doesn't matter what you wear...just watch out for other drivers on the road."
else
puts "Long sleeves and pants always do the trick when you sunburn in under 10 minutes, regardless of the weather."
end

